																													E-Verify Program Extended to September 30, 2016
																													<br>Employers Still Required to Complete Form I-9
																													<br>
																												</div>
																												<div style="text-align: left;" align="left">
																													<br> Recent legislation (§ 572) extends the federal E-Verify program to
																													<strong>September 30, 2016</strong>. The E-Verify program will require further authorization from the federal government in order to continue beyond the date set forth in the law.
																													<br>
																													<br>
																													<a style="color: rgb(0, 61, 114); text-decoration: underline;" rel="nofollow" track="on" shape="rect" href="http://www.uscis.gov/e-verify" linktype="1" target="_blank">E-Verify</a> is an Internet-based system that allows an employer, using information reported on an employee's Form I-9, to determine the eligibility of that employee to work in the United States. While participation in E-Verify is voluntary for most businesses, some companies may be required by state law or federal regulation to use E-Verify. The program is mandatory for employers with certain federal contracts or subcontracts that contain the
																													<a style="color: rgb(0, 61, 114); text-decoration: underline;" rel="nofollow" track="on" shape="rect" href="http://www.uscis.gov/sites/default/files/USCIS/E-Verify/Customer%20Support/E-Verify%20User%20Manual%20for%20Federal%20Contractors_Final.pdf" linktype="1" target="_blank">Federal Acquisition Regulation E-Verify clause</a>. There is no charge to employers to use E-Verify.
																													<br>
																													<br>
																													<strong>Note that E-Verify does not replace the legal requirement to complete and retain <a style="color: rgb(0, 61, 114); text-decoration: underline;" rel="nofollow" track="on" shape="rect" href="http://www.uscis.gov/i-9" linktype="1" target="_blank">Form I-9</a></strong>,
																													<em>Employment Eligibility Verification</em>. Federal law requires all employers to verify the identity and employment authorization of each person they hire by completing and retaining Form I-9. E-Verify verifies the employment authorization of new hires based on the information provided on Form I-9. Form I-9 must still be retained and stored in either paper, electronic, or microfilm/microfiche format.
