																														<span> <span>Among other issues, the DOL is requesting input on whether:</span></span>
																													</div>
																													<ul>
																														<li><span>The $455 per week threshold should be increased based on the rate of inflation.</span>
																														</li>
																														<li><span>Salary thresholds should be set based on state, metropolitan area, or the size of the employer.</span>
																														</li>
																														<li><span>Different salary thresholds should be set for the executive, administrative, and professional employee exemptions.</span>
																														</li>
																														<li><span>Salary thresholds and the highly compensated employee total annual compensation level should be automatically updated on a periodic basis.</span>
																														</li>
																													</ul>
