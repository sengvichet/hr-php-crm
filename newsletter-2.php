																														<li><span>Raising the salary threshold from $455 a week to $913 per week (or $47,476 annually) for a full-year worker;</span>
																														</li>
																														<li><span>Setting the highly compensated employee total annual compensation level at $134,004 annually;</span>
																														</li>
																														<li><span>Generally allowing employers to use nondiscretionary bonuses, incentives, and commissions to satisfy up to 10%of the new standard salary level; and</span>
																														</li>
																														<li><span>Establishing a mechanism for automatically updating the salary and compensation thresholds every 3 years, beginning in 2020.</span>
																														</li>
																													</ul>
																													<div>
																														<span> Enforcement of the 2016 final rule was halted by a federal judge in December 2016, and remains the subject of litigation.</span>
																													</div>
