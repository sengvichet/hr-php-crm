																														<div><strong>Use of Criminal History Information</strong><br>
																														</div>
																														<div><strong><br></strong>
																														</div>
																														<div>Effective July 1, employers may not consider any non-felony convictions related to marijuana possession that is more than two years old. Employers are also prohibited from using criminal history information that has an "adverse impact" on employees based on a protected class (e.g. race, national origin) unless the information is job-related and consistent with business necessity. The FEHC developed new standards that employers must meet and a complex procedural process that must be followed when considering criminal convictions in hiring. <br>
