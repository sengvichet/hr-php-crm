																															<span>Decision May Impact Workplace Policies and Practices</span>
																														</div>
																														<div>
																															<span><br> The California Court of Appeal (Fourth Appellate District) has released an opinion stating that employers may provide waiting periods before employees become eligible to earn vacation.</span>
																														</div>
																														<div>
																															<span><br></span>
																														</div>
																														<div>
																															<span>According to the opinion, an employer may provide a waiting period before an employee becomes eligible to earn vacation, and if the employer's policy is clearly stated, the waiting period policy is enforceable. If vacation pay is not earned, it is not vested, and therefore there is no entitlement to vacation pay at the termination of the employment relationship.</span>
																														</div>
																														<div>
																															<span><br> An employer may lawfully decide it will not provide paid vacation. An employer can also provide paid vacation after a specified waiting period. According to the court, this is similar to an employer's authority to limit the amount of vacation pay that may be earned.</span>
																														</div>
																														<div>
																															<span><br></span>
																														</div>
																														<div>
																															<span>Under <a style="color:rgb(0, 61, 114);font-weight:bold;text-decoration:underline;" rel="nofollow" track="on" href="http://bit.ly/2fJjx9l" shape="rect" linktype="1" target="_blank">state law</a>, whenever an employer policy (or contract of employment) provides for paid vacations, and an employee is terminated without having taken off his or her vested vacation time, all vested vacation generally must be paid as wages at his or her final rate in accordance with such policy (or contract) respecting eligibility or time served. An employer policy (or employment contract) may not provide for forfeiture of vested vacation time upon termination. Vested vacation pay cannot be taken away and all vacation pay is vested when earned. However, there is no legal requirement in California that an employer provide its employees with either paid or unpaid vacation time.</span>
