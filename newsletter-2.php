																													<span style="font-size:11.0pt;font-family:&quot;Calibri&quot;,sans-serif;">For September's National Preparedness Month, the IRS is offering the following guidance to those who may be affected by&nbsp;storms, fires, floods, or other disasters:</span>
																												</div>
																												<ul type="disc">
																													<li style="tab-stops:list .5in;background:white;line-height:normal;"><b>Create electronic copies of key documents.</b>&nbsp;Keep a duplicate set of key documents (e.g., bank statements and tax returns) in a safe place. Much financial information is available online, and electronic documents can be downloaded to a storage device such as an external hard drive or USB flash drive, or burned to a CD or DVD. Even if original documents are only provided on paper, these can be scanned into an electronic format.</li>
																													<li style="tab-stops:list .5in;background:white;line-height:normal;"><b>Document valuables.&nbsp;</b>Documenting valuables (such as by photographing or videotaping) ahead of time will make it easier to quickly claim any available insurance and tax benefits after a disaster strikes. Photos should be stored outside the disaster area.</li>
																													<li style="tab-stops:list .5in;background:white;line-height:normal;"><b>Check on fiduciary bonds.</b>&nbsp;Employers who use payroll service providers should ask the provider if it has a fiduciary bond in place. The bond could protect the employer in the event of default by the payroll service provider.</li>
																													<li style="tab-stops:list .5in;background:white;line-height:normal;"><b>Update emergency plans.</b>&nbsp;Because a disaster can strike at any time, be sure to review emergency plans annually. When employers hire new employees or when a company or organization changes functions, emergency plans should be updated accordingly, and employees should be informed of the changes.</li>
																													<li style="color:#666666;tab-stops:list .5in;background:white;line-height:normal;"><span style="color:windowtext;"><b>How to Contact the IRS.</b></span><span style="color:windowtext;">&nbsp;In the case of a federally declared disaster, an affected business can call 1-866-562-5227 to speak with an IRS specialist trained to handle disaster-related issues. Back copies of previously-filed tax returns and all attachments, including Forms W-2, can be requested by filing</span>&nbsp;<span style="color:windowtext;"><a style="color:rgb(0, 61, 114);font-weight:bold;text-decoration:underline;" rel="nofollow" track="on" href="https://www.irs.gov/pub/irs-pdf/f4506.pdf" shape="rect" linktype="1" target="_blank">Form 4506</a></span><span style="color:windowtext;">,&nbsp;<i>Request for Copy of Tax Return</i>.</span>
																													</li>
