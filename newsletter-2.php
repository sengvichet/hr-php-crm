																													Proprietary &amp; Confidential
																													<br>
																												</div>
																												<div style="text-align: left;" align="left">
																													<br> You know what they say about people who assume. When it comes to a company's intellectual property, never assume anything. Just because you think something is proprietary and confidential doesn't mean that it is, or that someone else will feel the same way you do. That's why it's a good idea to always err on the side of caution and make sure any documents that are part of a company's intellectual property are labeled as such, password protected if necessary, and mentioned in the employee handbook as such. This may not keep them from getting stolen, but it will help a company should it need to file suit.
																													<br>
																													<br>On
																													<em>Society for Human Resource Managment</em> an article titled,
																													<br>
																													<a style="color: blue; text-decoration: underline;" rel="nofollow" track="on" href="http://www.shrm.org/hrdisciplines/safetysecurity/articles/pages/identify-trade-secrets-ip-theft-litigation.aspx" shape="rect" linktype="1" target="_blank">Identify Your Trade Secrets to Prevail in IP Theft Litigation</a>, lists a case where information was taken and used by a former employee because it wasn't properly labeled as proprietary and confidential. Furthermore, it wasn't identified in the employee handbook as a trade secret, nor was the employee required to sign a noncompete agreement. The issue at hand was a list of people, including their names, addresses, phone numbers, etc., held by the company. An employee took that list and used it for a competing business. The argument was that everyone within the company understood that the list was only for business purposes and was not publicly known, nor available to the public. The court, however, disagreed and ruled that the list was not a trade secret or confidential and proprietary information. The list was available to all staff and to the people on the list, so the company wasn't trying to guard the secrecy of the information. Furthermore, most of the information was available in the public domain.
																													<br>
																													<br>So, what is the lesson here for employers? Regardless of what the information may be, what technologies it may contain, or who has access to it, make sure everyone knows that it's part of the company's intellectual property and put in place safeguards that ensure this.
																													<br>
																													<br>What kinds of safeguards are necessary? According to the article, as long as a company takes reasonable precautions by taking time to set up a system to protect information believed to be important, confidential, or proprietary, the system doesn't have to be perfect.
																													<br>
																													<br>So by limiting access to the information, keeping it secure, informing everyone who has access that the information is confidential, and designating the information as such in the employee handbook. It's also a good idea, according to the article, to remind departing employees during their exit interview about any confidentiality obligations.
																													<br>
																													<br>If something is worth protecting, then it's worth the extra time and effort needed to ensure its security. Proprietary and confidential mean just that, and these terms should be taken seriously and not applied haphazardly.
