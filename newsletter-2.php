																														</div><br>
																														<div>Employers are prohibited from discriminating against employees who are transitioning or have transitioned their birth gender. The new rules specify that employers must respect an employee's request to be identified by a preferred gender, name or pronoun, including gender-neutral pronouns. An employer can be held liable for a FEHA violation if the employer fails to abide by the employee's stated preference.<br>
																														</div><br>
